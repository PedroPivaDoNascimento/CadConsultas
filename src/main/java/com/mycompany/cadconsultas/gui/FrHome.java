    private void JSONToLists() {
        String filePathConsulta = "Consultas.json";
        String consultasData = FilePersistence.loadFromFile(filePathConsulta);
        if (!consultasData.isEmpty())
            this.consultasCadastradas = JSONConsultasService.JSONToConsultas(consultasData);
        
        String filePathMedicos = "Medicos.json";
        String medicosData = FilePersistence.loadFromFile(filePathMedicos);
        if (!medicosData.isEmpty())
            this.medicosCadastrados = JSONMedicosService.JSONToMedicos(medicosData);
        
        String filePathPacientes = "Pacientes.json";
        String pacientesData = FilePersistence.loadFromFile(filePathPacientes);
        if (!pacientesData.isEmpty())
            this.pacientesCadastrados = JSONPacientesService.JSONToPacientes(pacientesData);
    }
    
