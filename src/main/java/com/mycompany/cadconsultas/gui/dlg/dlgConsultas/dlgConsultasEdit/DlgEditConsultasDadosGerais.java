    private void voltarCamposOriginal() {
        this.edtData.setText(this.editedConsulta.getData());
        this.edtId.setText(this.editedConsulta.getId());
    }
    
   
    private void consultaToCampos(Consulta edited) {
        if (edited != null) {
            this.edtData.setText(edited.getData());
            this.edtId.setText(edited.getId());
        } else {
            JOptionPane.showMessageDialog(this, "Esse aluno não está cadastrado");
        }
    }
    
    private Consulta camposToConsulta() {
        Consulta newData = new Consulta();
        
        newData.setData(this.edtData.getText());
        newData.setId(this.edtId.getText());
       
        return newData;
    }

