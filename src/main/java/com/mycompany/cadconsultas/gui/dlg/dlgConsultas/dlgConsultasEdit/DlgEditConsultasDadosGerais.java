    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Consulta newData = this.camposToConsulta();
        
        if (this.consultasCadastradas.edit(this.editedConsulta.getId(), newData)) {
            JOptionPane.showMessageDialog(this, "Consulta editada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Não foi possível editar essa consulta");
        }
        
        this.voltarCamposOriginal();
        this.edtConsultasCadastradas.setText(this.consultasCadastradas.toString());
    }//GEN-LAST:event_btnSaveActionPerformed

