    private void saveToJSON() {
        String path = "Consultas.json";
        String data = JSONConsultasService.consultasToJSON(this.consultasCadastradas);
        FilePersistence.saveToFile(data, path);
    }    
    private void clearCampos() {
        this.edtData.setText("");
        this.edtId.setText("");
    }
    
    private Consulta camposToConsulta() {
        Consulta dadosPreenchidos = new Consulta();
 
        dadosPreenchidos.setData(this.edtData.getText());
        dadosPreenchidos.setId(edtId.getText());
        
        return dadosPreenchidos;
    }
    
