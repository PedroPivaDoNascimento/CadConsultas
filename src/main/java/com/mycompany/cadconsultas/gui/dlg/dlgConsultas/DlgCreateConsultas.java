    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Consulta newConsulta = this.camposToConsulta();
        
        if (this.consultasCadastradas.add(newConsulta)) {
            JOptionPane.showMessageDialog(this, "Consulta cadastrada com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Essa consulta já está cadastrada");
        }
        
        this.clearCampos();
        
        this.edtConsultasCadastradas.setText(consultasCadastradas.toString());
        this.saveToJSON();
    }//GEN-LAST:event_btnCadastrarActionPerformed

