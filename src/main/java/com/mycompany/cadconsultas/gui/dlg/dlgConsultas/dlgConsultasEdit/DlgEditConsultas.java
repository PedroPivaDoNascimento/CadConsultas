    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String editedId = this.edtEditedId.getText();
        Consulta editedConsulta = this.consultasCadastradas.findConsultaById(editedId);
        
        if (editedConsulta != null) {
            this.setVisible(false);
            DlgEditConsultasOptions screenConsultasEditOptions = new DlgEditConsultasOptions(frParent, true, consultasCadastradas, editedConsulta, medicosCadastrados, pacientesCadastrados);
            screenConsultasEditOptions.setVisible(true);
            this.edtConsultasCadastradas.setText(this.consultasCadastradas.toString());
            this.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Essa consulta n√£o foi cadastrada");
        }
    }//GEN-LAST:event_btnEditActionPerformed

